/**
 * EXE 빌드 시 deploy/local.env → embedded-install-env.gen.ts (Git 제외, EXE에 포함)
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const src = path.join(root, 'deploy', 'local.env');
const out = path.join(root, 'rank-check', 'launcher', 'embedded-install-env.gen.ts');

function parseEnv(content) {
  const out = {};
  for (const line of content.split(/\r?\n/)) {
    const t = line.trim();
    if (!t || t.startsWith('#')) continue;
    const eq = t.indexOf('=');
    if (eq <= 0) continue;
    const k = t.slice(0, eq).trim();
    let v = t.slice(eq + 1).trim();
    if (
      (v.startsWith('"') && v.endsWith('"')) ||
      (v.startsWith("'") && v.endsWith("'"))
    ) {
      v = v.slice(1, -1);
    }
    if (k) out[k] = v;
  }
  if (!out.SUPABASE_URL && out.NEXT_PUBLIC_SUPABASE_URL) {
    out.SUPABASE_URL = out.NEXT_PUBLIC_SUPABASE_URL;
  }
  return out;
}

if (!fs.existsSync(src)) {
  console.error('[generate-embedded-env] missing:', src);
  console.error('  Copy deploy/local.env.example → deploy/local.env and add keys.');
  process.exit(1);
}

const env = parseEnv(fs.readFileSync(src, 'utf8'));
const required = ['SUPABASE_URL', 'SUPABASE_SERVICE_ROLE_KEY'];
for (const k of required) {
  if (!env[k]) {
    console.error(`[generate-embedded-env] missing ${k} in deploy/local.env`);
    process.exit(1);
  }
}
if (env.SUPABASE_SERVICE_ROLE_KEY.startsWith('eyJ')) {
  console.error('[generate-embedded-env] deploy/local.env must use sb_secret_ key, not Legacy JWT');
  process.exit(1);
}

const body = JSON.stringify(env, null, 2);
const ts = `/** AUTO-GENERATED by scripts/generate-embedded-env.mjs — do not commit */\nexport const EMBEDDED_INSTALL_ENV: Record<string, string> = ${body};\n`;
fs.writeFileSync(out, ts, 'utf8');
console.log('[generate-embedded-env] wrote', out);
